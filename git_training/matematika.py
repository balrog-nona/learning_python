import math

class Matematika:

    def scitani(self, a, b):
        return a + b

    def odcitani(self, a, b):
        return a - b

    def nasobeni(self, a, b):
        return a * b

    def deleni(self, a, b):
        return a / b

    def mocnina(self, a, b):
        return a ** b

    def odmocnina(self, a):
        return math.sqrt(a)

    def obvod_ctverce(self, a):
        return 4 * a