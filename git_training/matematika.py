import math

class Matematika:

    def scitani(self, a, b):
        # komentar
        return a + b

    def odcitani(self, a, b):
        return a - b

    def nasobeni(self, a, b):  # tady se vysvetli princip metody
        return a * b

    def deleni(self, a, b):
        return a / b

    def mocnina(self, a, b):  # komentar
        return a ** b

    def odmocnina(self, a):
        return math.sqrt(a)

    def obvod_ctverce(self, a):
        # komentar
        return 4 * a

    def obvod_obdelnika(self, a, b):
        return 2 * (a + b)
